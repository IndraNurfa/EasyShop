package entity;

/**
 *
 * @author Nurfa
 */
public class BarangEntity {
    String id, merek, tipe;
    int berat, harga;

    public BarangEntity(String id, String merek, String tipe, int berat, int harga) {
        this.id = id;
        this.merek = merek;
        this.tipe = tipe;
        this.berat = berat;
        this.harga = harga;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMerek() {
        return merek;
    }

    public void setMerek(String merek) {
        this.merek = merek;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }

    public int getBerat() {
        return berat;
    }

    public void setBerat(int berat) {
        this.berat = berat;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }
}
