    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String username = tfUsername.getText();
        String nama = tfNama.getText();
        String email = tfEmail.getText();
        String pass = String.valueOf(tfPassword.getPassword());
        String passConfirm = String.valueOf(tfConfirmPassword.getPassword());
        RegisterEntity reg = new RegisterEntity(username, pass);
        RegisterEntity register = new RegisterEntity(username, nama, email);
        if (username.equals("") || nama.equals("") || email.equals("") || pass.equals("") || passConfirm.equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua form");
        } else {
            if (pass.equals(passConfirm)) {
                boolean status = insertLogin(reg);
                if (status) {
                    insertData(register);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password tidak sama");
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

