    private boolean insertLogin(RegisterEntity r) {
        try {
            boolean statusLogin = dbRegister.loginInsert(r);
            if (statusLogin) {
                JOptionPane.showMessageDialog(null, "Berhasil");
                return true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Username tidak tersedia");
        }
        return false;
    }

    private void insertData(RegisterEntity r) {
        try {
            boolean statusData = dbRegister.dataInsert(r);
            if (statusData) {
                UserMainFrame userFrame = new UserMainFrame();
                userFrame.setVisible(true);
                dispose();
            }
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

